package com.hackers;

/**
 * @author manu
 *
 */
public class Values {

	public int a;
	public int b;
	public int n;
	/**
	 * @return the a
	 */
	public int getA() {
		return a;
	}
	/**
	 * @param a the a to set
	 */
	public void setA(int a) {
		this.a = a;
	}
	/**
	 * @return the b
	 */
	public int getB() {
		return b;
	}
	/**
	 * @param b the b to set
	 */
	public void setB(int b) {
		this.b = b;
	}
	/**
	 * @return the n
	 */
	public int getN() {
		return n;
	}
	/**
	 * @param n the n to set
	 */
	public void setN(int n) {
		this.n = n;
	}
	/**
	 * 
	 */
	public Values() {
		super();
	}
	/**
	 * @param a
	 * @param b
	 * @param n
	 */
	public Values(int a, int b, int n) {
		super();
		this.a = a;
		this.b = b;
		this.n = n;
	}
	
	
}
